            caD.renovacion(cal); 
            
            jtID.setText("");
            jDateChooser4.setDate(null);
            jDateChooser5.setDate(null);
            jDateChooser6.setDate(null);
        
            jTextField3.setText("");
            jTextField5.setText("");
                
