    private void cargarCombo(){ 
        
        InquilinoData inqD = new InquilinoData();
        ArrayList<Inquilino> inquilino = new ArrayList<>();

        for (Inquilino inqui : inqD.obtenerInquilinosdeBaja()) {
            inquilino.add(inqui);
            jcInquilinos.addItem(inqui);
        }

    }
    
